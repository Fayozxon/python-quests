# Qayiqlar turg'un suvda ketmoqda

# Birinchi qayiq tezligi(km/soat):
speed_1 = 4
# Ikkinchi qayiq tezligi(km/soat):
speed_2 = 2
# Ular orasidagi masofa(km):
distance = 24

# Ular qancha vaqtdan keyin uchrashadi?
meet_time = distance / (speed_1 + speed_2)
print(meet_time, 'soatdan keyin uchrashadi')

# Qancha vaqtdan keyin ular orasidagi masofa 12km bo'ladi?
distance_2 = 12
meet_time_2 = distance_2 / (speed_1 + speed_2)
print(meet_time_2, 'soatdan keyin ular orasidagi masofa 12km bo\'ladi')
